# Problem Statement
# Create a program to print the date in the given format.

# Get three string inputs: day, month, and year.
# Print them in this format: month/day/year
# Hint: Use the + operator to concatenate strings.

# Example
# Test Input

# 10
# 01
# 2000
# Expected Output

# 01/10/2000

day = input()
month = input()
year = input()

# print the date in the given format
print(f"{month}/{day}/{year}")