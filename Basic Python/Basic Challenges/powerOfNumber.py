# Program Description
# Write a program to find the power of a number using the math module.

# Import the math module.
# Get an integer input in variable num.
# Calculate the power of num raised to 4 using pow() function of math module.
# Print the result.

# import module math 
import math


# get an integer user input for num variable
num = int(input())

# calculate the power of num raised to 4
# using pow() from module math and print it
power = math.pow(num,4)
print(power)