# Merge Two Lists
# Easy
# Problem Description
# Create a Python program to merge two lists containing string elements.

# Example
# Test Input

# For two lists ['apple', 'banana', 'mango'] and ['potato', 'brinjal', 'cauliflower']
# Expected Output

# ['apple', 'banana', 'mango', 'potato', 'brinjal', 'cauliflower']


fruits = ['Apple', 'Banana', 'Mango']
vegetables = ['Potato', 'Brinjal', 'Cauliflower']

result = fruits + vegetables
print(result)


# Note: While merging two lists, the new list will have duplicate elements if the same element is present in both lists