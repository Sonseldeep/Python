# Challenge:
# Merge Two Lists of Different Types
# Easy
# Problem Description
# Create a Python program to merge a list of strings and a list of numbers.

# Example
# Test Input

# For two lists ['apple', 'orange', 'banana'] and [5, 8, 10]
# Expected Output

# ['apple', 'orange', 'banana', 5, 8, 10]


# list of string types
list1 = ['apple', 'orange', 'banana']

# list of numbers
list2 = [5, 8, 10]

# merge two lists
merged_list = list1 + list2

# print the merged_list
print(merged_list)